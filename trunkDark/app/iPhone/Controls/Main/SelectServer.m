}


// Customize the appearance of table view cells.
- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    
    static NSString *CellIdentifier = @"ServerCell";
    
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier];
    if (cell == nil) {
        cell = [[[UITableViewCell alloc] initWithStyle:UITableViewCellStyleDefault reuseIdentifier:CellIdentifier] autorelease];
    }
    ServerInfo *si = [storage.Servers objectAtIndex:indexPath.row];
	
    cell.textLabel.text = [si alias];
	NSLog(@"selected server:%@", [storage selected_server]);
	if([[si alias] isEqualToString:[storage selected_server]])
		[cell setAccessoryType:UITableViewCellAccessoryCheckmark];
	else
		[cell setAccessoryType:UITableViewCellAccessoryNone];
	   
    return cell;
}


- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath 
{
    ServerInfo *si = [storage.Servers objectAtIndex:indexPath.row];
    //if (![si.alias isEqualToString:storage.selected_server])
//    {
//        [storage clearServerResults];
//    }
	storage.selected_server = si.alias;
	storage.serverResult = nil;
	storage.tokkenResult = nil;
    [storage loadFavorites];
	[[NSNotificationCenter defaultCenter] postNotificationName:@"serverDlgClosed" object:self];
}


